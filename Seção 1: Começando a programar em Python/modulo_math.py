import math

# Retorna o menor inteiro não menor que x
print(math.ceil(3.14))

# Retorna o valor absoluto de um número
print(math.fabs(4))

# Retorna o maior número inteiro não maior que x
print(math.floor(3.14))

# Retorna retornos exponenciais de x: e^x
print(math.exp(2))

# Retorna o logaritmo
print(math.log10(10))

# Retonar o resultado de um número elevado a outro número
print(math.pow(2, 10))

# Retorna a raiz quadrada de um número
print(math.sqrt(25))

# Retonar funções trigonométricas
print(math.cos(0))
print(math.sin(30))

# Retorna o valor do PI
print(math.pi)

# Retorna a constante matemática
print(math.e)

